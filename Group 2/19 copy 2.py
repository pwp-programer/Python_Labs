import turtle

def pixel(color):
    turtle.color(color)
    turtle.begin_fill()
    for i in range(4):
        turtle.forward(10)
        turtle.right(90)
    turtle.end_fill()

def pixel_r(color):
    turtle.forward(10)
    pixel(color)   

def pixel_l(color):
    turtle.backward(10)
    pixel(color)

def down(x):
    turtle.penup()
    turtle.home()
    turtle.left(90)
    turtle.backward(x)
    turtle.right(90)
    turtle.pendown()
    
